export function getGovernorateBySlug(
  slug: string
): GovernorateMarketData | undefined {
  return ALL_GOVERNORATES.find((g) => g.slug === slug);
}

export function getWilayatBySlug(
  governorateSlug: string,
  wilayatSlug: string
): WilayatMarketData | undefined {
  const gov = getGovernorateBySlug(governorateSlug);
  return gov?.wilayats.find((w) => w.slug === wilayatSlug);
}

export function getAllWilayats(): WilayatMarketData[] {
  return ALL_GOVERNORATES.flatMap((g) => g.wilayats);
}
