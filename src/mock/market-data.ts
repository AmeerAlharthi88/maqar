// ── Shared monthly trend helper ──────────────────────────────────────────────
function buildTrend(
  basePrice: number,
  baseRent: number,
  baseCount: number
): MonthlyTrendPoint[] {
  const months = ["ديسمبر", "يناير", "فبراير", "مارس", "أبريل", "مايو"];
  return months.map((month, i) => ({
    month,
    avgSalePrice: Math.round(basePrice * (1 + (i * 0.008))),
    avgRentPrice: Math.round(baseRent * (1 + (i * 0.005))),
    listingCount: Math.round(baseCount * (0.9 + (i * 0.04))),
  }));
}

